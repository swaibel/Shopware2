CREATE VIEW [dbo].[vw_DailySalesTotals]
AS
SELECT        Shop_id, ClosedDate, SUM(PartsDollars) AS Parts, SUM(LaborDollars) AS Labor, SUM(HazmatsDollars) AS Hazmats, SUM(Discounts) AS Discounts, SUM(Warranty) AS Warranty, SUM(CustomerSat) AS CustomerSat, 
                         SUM(ShopSuppliesDollars) AS ShopSupplies, SUM(SubletDollars) AS Sublet, SUM(TotalTax) AS TotalTax
FROM            dbo.vw_DailySales
GROUP BY Shop_id, ClosedDate
GO
